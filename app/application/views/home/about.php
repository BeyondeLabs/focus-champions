<h3><i class="fa fa-gears"></i> How it Works</h3>
<ul>
	<li><strong>Registration</strong> - supporters (champions) sign up on the platform, with very basic details.
	 Mainly name, CU/University, gender, contacts. </li>
	<li><strong>Commitment</strong>  - This is the second stage on 
		registration, a supporter now makes a commitment, chooses one 
		of the options: one-off, monthly, bi-annually and annually, and 
		specifies the amount the the start and end dates.</li>
	<li><strong>Periodic Contributions</strong> - this is the actual 
		contribution, mainly honoring the commitment made. The system 
		automatically send gentle reminders to the supporters 
		concerning their commitment around the due dates.</li>
	<li><strong>Inviting Friends (network effect)</strong> - For the growth 
		of the platform, there’s a mechanism where already opted-in supporters
		 can be able to invite their friends/colleagues to join the platform 
		 as supporters (champions).</li>
</ul>

